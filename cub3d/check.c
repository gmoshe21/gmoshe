#include "ft_cub.h"

int		check(t_cub *cub)
{
	int		x;
	int		y;
	char	k;

	y = 0;
	while (cub->map[y])
	{
		x = 0;
		while (cub->map[y][x])
		{
			if (cub->map[y][x] == '0' && (cub->map[y][x + 1] == ' ' ||
			cub->map[y][x - 1] == ' ' || cub->map[y + 1][x] == ' '
			|| cub->map[y - 1][x] == ' '))
				return (0);
			x++;
		}
		y++;
	}
	return (1);
}